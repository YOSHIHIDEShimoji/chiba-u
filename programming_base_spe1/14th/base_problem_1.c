#include <stdio.h>

int main(void)
{ 
	int year; 
	for (year = 1896; year <= 2040; year++) {
		if (year % 400 == 0) {
			printf("%d ", year);
		}
		if (year % 4 == 0 && year % 100 != 0) {
			printf("%d ", year);
		}
	}
	printf("\n");
	return 0;
	
}

