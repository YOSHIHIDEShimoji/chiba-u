int include_3(int n)
{
	if (n == 0) {
		return 0;
	}
	
	if (n % 3 == 0) {
		return 1;
	}

	while (n != 0) {
		if (n % 10 == 3) {
			return 1;
		} 
		n /= 10;
	}
	return 0;
}

